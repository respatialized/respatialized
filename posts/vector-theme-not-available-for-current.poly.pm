◊(define-meta title "Vector Theme not available for current version of MediaWiki")
◊(define-meta published "2010-06-16")
◊(define-meta topics "MediaWiki")

You may have noticed ◊link["http://en.wikipedia.org/wiki/Two_Harbors,_Minnesota"]{Wikipedia} has ◊link["http://blog.wikimedia.org/2010/wikimedia-gets-ready-for-some-big-changes/"]{a new interface} these days. If you run your own ◊link["http://www.mediawiki.org/wiki/MediaWiki"]{MediaWiki} website, the obvious question in your mind is, how can I get these improvements on my site too?

This information is surprisingly hard to find, but the short answer is: you can’t yet.

◊blockquote{
◊strong{◊emph{How can I use Wikipedia’s new skin (Vector) on my wiki?}}

Vector is not available in MediaWiki versions prior to 1.16, and will be made default skin in 1.17.
}

— buried in the ◊link["http://www.mediawiki.org/wiki/FAQ#How_can_I_use_Wikipedia.27s_new_skin_.28Vector.29_on_my_wiki.3F"]{MediaWiki FAQ}

As of the time of this writing (June 2010), 1.16 is still in beta, and 1.17 has no planned release date. Check the ◊link["http://www.mediawiki.org/wiki/Version_lifecycle"]{MediaWiki version lifecycle} for current information.
